export function getServiceBySlug(slug: string): ServicePage | undefined {
  const base = SERVICE_PAGES.find((p) => p.slug === slug);
  if (!base) return undefined;

  const catalogPrices = getPricesForServiceSlug(slug, base.category);
  const prices = catalogPrices.length > 0 ? catalogPrices : base.prices;
  const priceFrom = catalogPrices.length > 0 ? getPriceFromForSlug(slug, base.priceFrom) : base.priceFrom;

  return { ...base, prices, priceFrom };
}

export function getAllServiceSlugs(): string[] {
  return SERVICE_PAGES.map((p) => p.slug);
}

